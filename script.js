function jog (x,y){

    var result = x + y;
    document.write("jogfol = " + result + "<br>");
}

function biyog (x,y){

    var result = x - y;
    document.write("biyogfol = " + result + "<br>");
}


function gun (x,y){

    var result = x * y;
    document.write("gunfol = " + result + "<br>");
}



jog(10,20);
biyog(10,50);
gun(10,50);
